package com.techelevator.view;

public class TextColors {

    static String blackTxt = "\u001B[30m";
    static String redTxt = "\u001B[31m";

    static String greenTxt = "\u001B[32m";
    static String yellowTxt = "\u001B[33m";
    static String blueTxt = "\u001B[34m";
    static String purpleTxt = "\u001B[35m";
    static String cyantxt = "\u001B[36m";
    static String whiteTxt = "\u001B[37m";

    static String resetTxtColor = "\u001B[0m";

    public static String getBlackTxt() {
        return blackTxt;
    }



    public static String getRedTxt() {
        return redTxt;
    }



    public static String getGreenTxt() {
        return greenTxt;
    }


    public static String getYellowTxt() {
        return yellowTxt;
    }


    public static String getBlueTxt() {
        return blueTxt;
    }


    public static String getPurpleTxt() {
        return purpleTxt;
    }



    public static String getCyantxt() {
        return cyantxt;
    }



    public static String getWhiteTxt() {
        return whiteTxt;
    }



    public static String getResetTxtColor() {
        return resetTxtColor;
    }

}